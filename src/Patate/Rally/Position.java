package Patate.Rally;

public class Position {
	private int X;
	private int Y;

	public Position() {
		super();
		X = 2;
		Y = 0;
	}

	public int getX() {
		return X;
	}

	public void setX(int x) {
		X = x;
	}

	public int getY() {
		return Y;
	}

	public void setY(int y) {
		Y = y;
	}
}
